async function getanli(now){
    ret = {}
    if(now){
        await $.get(localStorage.getItem('apiURL')+'/playlist/detail?id=8963738332',(data,status)=>{
            ret['count'] = data['playlist']['trackCount']
            songs = []
            for(num1 in data['playlist']['tracks']){
                l = {}
                i = data['playlist']['tracks'][num1]
                l['name'] = i['name']
                l['image'] = i['al']['picUrl']
                arts_str = ""
                for(num2 in i['ar']){
                    i2 = i['ar'][num2]
                    arts_str += i2['name']+" "
                }
                l['arts'] = arts_str
                l['id'] = i['id']
                songs.push(l)
            }
            ret['songs'] = songs
        })
    }else{
        ret = data
    }
    return ret
}